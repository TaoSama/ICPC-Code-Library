const int N = 2e5 + 10, INF = 0x3f3f3f3f, MOD = 1e9 + 7;

int n, m;
vector<int> G[N];

int col[N];
bool dfs(int u, int c) {
    col[u] = c;
    for(int v : G[u]) {
        if(col[v] == c) return false;
        if(col[v] == -1 && !dfs(v, c ^ 1)) return false;
    }
    return true;
}

