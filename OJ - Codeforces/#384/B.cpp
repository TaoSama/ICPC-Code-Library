typedef long long LL;
LL n, k;

LL dfs(LL n, LL k) {
    LL mid = (1LL << n) / 2;
    if(mid == k) return n;
    else if(mid < k) return dfs(n - 1, k - mid);
    else return dfs(n - 1, k);
}

