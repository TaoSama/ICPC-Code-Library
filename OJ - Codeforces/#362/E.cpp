        LL two = 2, one = -1;
        for(int i = 1; i <= n; ++i) {
            LL x; scanf("%I64d", &x);
            two = ksm(two, x);
            one = ksm(one, x);
        }
        two = two * ksm(2, MOD - 2) % MOD;
        one = one * ksm(-1, MOD - 2) % MOD;
        LL up = (two - one + MOD) % MOD;
        up = up * ksm(3, MOD - 2) % MOD;
