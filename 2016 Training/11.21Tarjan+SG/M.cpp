const int N = 1e5 + 10, INF = 0x3f3f3f3f, MOD = 1e9 + 7;

int n;
vector<int> G[N];

int dfs(int u, int f) {
    int ret = 0;
    for(int v : G[u]) {
        if(v == f) continue;
        ret ^= (1 + dfs(v, u));
    }
    return ret;
}

